de síntomas elegidos por el usuario es decir que a menor elección de 
síntomas menor la probabilidad. Esta probabilidad nunca llega a 
100% ya que tumedibot.com no es un médico real. tumedibot.com es el 
resultado de un algoritmo de diagnóstico basado en la cantidad de 
síntomas de enfermedad relacionados con un órgano en particular.
</li>
