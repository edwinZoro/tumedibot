  <h2 class="title_next" >Introducción</h2>
  <p class="parrafos">tumedibot.com es una aplicación web que sugiere una enfermedad relacionada 
con los órganos de la anatomía humana basados en el análisis de la elección 
de síntomas que agrega el usuario. Además otorga la posibilidad a los 
especialistas médicos de llenar un perfil y mostrarse en listados de búsqueda 
de especialidades médicas y ser encontrado por los usuarios
</p>
  <h2 class="title_next"> ¿Cómo trabaja tumedibot.com?</h2>
  <p class="parrafos">tumedibot.com recopila los datos de los síntomas de enfermedades relacionados a 
un órgano en particular de un usuario específico y los compara dependiendo de 
la cantidad de síntomas indicados por el usuario, se obtendrá una sugerencia 
de enfermedad si la cantidad de síntomas elegidos es preponderante con 
respecto a dicha enfermedad.  </p>
  <p class="parrafos">Entre las ventajas cabe destacar:</p>
  <ul class="parrafos_b">
    <li> <b>Velocidad:</b> Tiempo de respuesta de sugerencia de enfermedad 
aproximadamente 6s.</li>
    <li><b>Diversidad de género: </b>  En tumedibot.com los géneros masculinos y 
femeninos son totalmente separados, por lo que una mujer puede 
evaluar sus síntomas por ejemplo en los senos o aparato reproductor.</li>
    <li> <b>Listados de búsqueda:</b> Encuentra especialidades médicas y filtro de 
búsqueda por nombre, precio de consulta y zona, además observa los 
perfiles médicos con detalles del especialista.
</li>
    <li> <b>Sugerencia de especialista:</b> Al obtener los resultados tumedibot.com es 
capaz de dar una sugerencia de las especialidades a la que debería 
asistir el usuario, además si quiere más información de la enfermedad 
tenemos enlaces hacia páginas externas del sitio que el usuario puede 
utilizar.
</li>
  </ul>
  <h2 class="title_next">Requisitos de sistema</h2>
  <p class="parrafos">tumedibot.com es una aplicación web responsive que se ejecuta en ordenadores y 
dispositivos móviles (teléfono inteligente, tableta) con conexión a internet. Entre 
los requisitos mínimos para ejecutarse están:</p>
  <ul class="parrafos_b">
    <li> 512M de RAM.</li>
    <li> Procesador de 1Ghz.</li>
    <li> Sistema operativo Windows, Mac, Linux, Android y cualquiera que pueda 
ejecutar un navegador web.</li>
    <li> Por ser una aplicación web sólo se ejecuta en navegadores como 
Google Crome, Firefox, Opera, Edge, Safari.</li>
    <li> Conexión a Internet es obligatoria para poder usar la aplicación web.</li>
  </ul>
  <br>
  <h2 class="title_next">Advertencia</h2>
  <ul class="parrafos_b">
    <li> <b>Es obligatorio tener mayoría de edad (18 años)</b> para usar la 
aplicación web.</li>
    <li><b>El resultado no es un diagnóstico.</b> El resultado es una sugerencia de 
una posible enfermedad solo con fines informativos y no constituye una 
consulta y/o asesoramiento médico. De hecho incitamos al usuario el 
uso de los perfiles médicos dentro de la aplicación web para llegar a 
este fin.</li>
    <li> <b>No usar la aplicación web en casos de emergencia.</b> De tener la 
sensación de que su vida está en peligro por su salud, llame a un 
número de emergencia como el 911
</li>
    <li> <b>Sus datos están seguros.</b> Sus datos están protegidos totalmente, si 
desea más información acerca de la protección de sus datos consulte en 
Términos y Condiciones Del Sitio y la Política de Privacidad.</li>
  </ul>
  <h2 class="title_next">Contraindicaciones para el uso</h2>
  <p class="parrafos">No utilices la aplicación web cuando tengas sospecha de una emergencia 
médica. En dicho caso, Inmediatamente busca obtener ayuda médica, medidas 
de emergencia y tratamiento lo antes posible, por ejemplo, llamando a una 
ambulancia o acudiendo al servicio de urgencias de un hospital. </p>
<p class="parrafos"><b>Estado de emergencia inminente para la salud:</b> un estado que consiste en 
la aparición súbita o prevista a corto plazo de síntomas de deterioro de la salud 
cuya consecuencia directa puede ser una grave disfunción corporal o la pérdida 
de la vida y que requiere medidas de rescate y tratamiento médico inmediato.<br>
Algunos ejemplos de los estados de emergencia para la salud:
</p>
<ul class="parrafos_b">
  <li> Pérdida de conocimiento.</li>
  <li> Convulsión.</li>
  <li> Insolación.</li>
  <li> Alteración del ritmo cardíaco. </li>
  <li> Disnea intensa.</li>
  <li> Reacciones alérgicas intensas y fuertes que produce erupción, 
hinchazón u otro síntoma y que puede ser consecuencia de alimento 
ingerido, medicina tomada o picadura/mordedura de animal o insecto 
venenoso.</li>
  <li> Hemorragias fuertes indetenibles.</li>
  <li> Intento de suicidio efectuado.</li>
  <li> Intoxicación producida por gases, productos químicos o medicamentos.</li>
  <li> Infarto.</li>
  <li> Agresión por enfermedad mental.</li>
  <li> Electrocución.</li>
  <li> Quemaduras graves.</li>
</ul>
  <p class="parrafos">Además de los ejemplos mencionados, cesa el uso de la aplicación y toma 
  medidas inmediatas para buscar atención médica adecuada en cualquier otra 
  situación en la que sospechas una emergencia médica.</p>
