síntomas de enfermedad que selecciono el usuario.</li>
    <li> <b>Sugerencia de especialistas:</b> tumedibot.com sugiere los especialistas 
médicos relacionados con el órgano humano y las enfermedades de 
ese órgano. Con el fin de que el usuario haga seguimiento de la 
enfermedad consultando un médico real.</li>
    <li> <b>Más información:</b> Si desea saber más información de la enfermedad 
